# Use a variable to reprsent a person's name, and then print that person's name in lowercase, uppercase, and title case.
first_name = "crystal"
last_name = "Rivas"
full_name = f"{first_name} {last_name}"
print(f"Hello, {full_name.title()}")
print(f"{full_name.upper()}")
print(f"{full_name.lower()}")